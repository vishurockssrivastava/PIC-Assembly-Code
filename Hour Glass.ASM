LIST P=18F4520
#INCLUDE<P18F4520.INC>
COUNTERH EQU 0X20
COUNTERL EQU 0X21
COUNTERJ EQU 0X08
ORG 0X00
GOTO START
START
	CLRF PORTB
	MOVLW 0X00
	MOVWF TRISB
LOOP
	MOVLW 0X01
	MOVWF PORTB
	CALL DELAY
	LUBE
		MOVLW 0X08
		MOVWF COUNTERJ
		DECFSZ COUNTERJ,F
	RETURN
	END

GOTO LOOP

DELAY
	MOVLW 0X02
	MOVWF COUNTERH
S1
	MOVLW 0X02
	MOVWF COUNTERL
	DECFSZ COUNTERL,F
	GOTO $-1
	DECFSZ COUNTERH,F
GOTO S1
RETURN
END