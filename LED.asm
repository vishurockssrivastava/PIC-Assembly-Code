LIST P=18F4520
#INCLUDE<P18F4520.INC>
COUNTERH EQU 0X20
COUNTERL EQU 0X21
ORG 0X00
GOTO START
START
	CLRF PORTB
	MOVLW 0X00
	MOVWF TRISB
LOOP
	CLRF PORTB
	CALL DELAY
	;SETF PORTB
	MOVLW 0X81
	MOVWF PORTB
	CALL DELAY
	MOVLW 0X42
	MOVWF PORTB
	CALL DELAY
	MOVLW 0X24
	MOVWF PORTB
	CALL DELAY
	MOVLW 0X18
	MOVWF PORTB
	CALL DELAY
GOTO LOOP

DELAY
	MOVLW 0X02
	MOVWF COUNTERH
S1
	MOVLW 0X02
	MOVWF COUNTERL
	DECFSZ COUNTERL,F
	GOTO $-1
	DECFSZ COUNTERH,F
GOTO S1
RETURN
END